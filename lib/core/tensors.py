"""
Семантика массивов / тензоров для пайплайнов TFR (time–frequency representation).

Конвенции по осям (NumPy ``X`` для Optuna / датасетов)
======================================================

========  ============================================================
Ось/буква  Смысл
========  ============================================================
``N``       Число эпох (или окон), размер выборки; **меняется** от сплита к сплиту.
``C``       Каналы (EEG/MEG и т.д.); **фиксируется** препроцессингом для датасета.
``F``       Бины частоты после crop; **фиксируется** конфигом (например ``patients.yaml``).
``T``       Бины времени после crop; **фиксируется** тем же конфигом.
``K``       Число классов (меток); обычно 2 для бинарной задачи.
========  ============================================================

**Вход в нейросеть (PyTorch)** — тот же логический объект, что и ``X[i]`` одной эпохи,
но батчем: ``(B, C, F, T)``, где ``B`` — размер minibatch (переменный шаг обучения).

Типы ниже — это **алиасы для читаемости**; статический анализатор не проверяет ранги
и формы. Инварианты задаются докстрингами и проверками ``shape`` / ``ndim`` в коде.
"""

from __future__ import annotations

from typing import TypeAlias

import numpy as np
from numpy.typing import NDArray

# --- NumPy: сырые эпохи до / после нормализации ---

FloatArray: TypeAlias = NDArray[np.floating]
"""Любой float-массив (часто ``float32`` после ``astype``)."""

TFRFeatureArray: TypeAlias = FloatArray
"""
Четырёхмерный массив эпох TFR, форма ``(N, C, F, T)``.

* ``N`` — эпохи; ``C, F, T`` согласованы с препроцессингом и crop.
* Используется как ``X`` в :func:`lib.optuna.engine.make_objective_engine`.
"""

EpochLabelsArray: TypeAlias = NDArray[np.integer]
"""
Целочисленные метки классов, форма ``(N,)``, значения ``0 .. K-1``.

Для ``sklearn`` / :class:`torch.nn.CrossEntropyLoss` метки — **не** one-hot.
"""

# --- Опционально: узкий dtype ---

Float32Array: TypeAlias = NDArray[np.float32]
Int64Array: TypeAlias = NDArray[np.int64]
